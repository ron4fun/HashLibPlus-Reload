		string HashOfABCDE = "1D7C3AA6EE17DA5F4AEB78BE968AA38476DBEE54842E1AE2856F4C9A5CD04D45DC75C2902182B07C130ED582D476995B502B8777CCF69F60574471600386639B";
		string HashOfDefaultDataWithHMACWithLongKey = "196FD60618333041460020DDAF61863C4AF5DA4FFE34FF08960F1F46368168A1716A769D0785DB42D547D3B089EF33B059C797D7E4BC61ECC5BB1717632510C1";
		string HashOfDefaultDataWithHMACWithShortKey = "439C673B33F0F6D9273124782611EA96F1BB62F90672551310C1230ADAAD0D40F63C6D2B17DAFECEFD9CE8848576001D9D68FAD1B9E7DDC146F00CEBE5AFED27";

		IHash HashInstance = HashFactory::Crypto::CreateSHA3_512();
